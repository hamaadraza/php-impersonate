    /**
     * Get configuration for a specific browser
     */
    public static function getConfig(string $browserName): array
    {
        $configs = self::getAllConfigs();

        if (! isset($configs[$browserName])) {
            throw new \InvalidArgumentException("Browser configuration not found: {$browserName}");
        }

        return $configs[$browserName];
    }

    /**
     * Get list of all available browser names
     */
    public static function getAvailableBrowsers(): array
    {
        return array_keys(self::getAllConfigs());
    }

    /**
     * Check if a browser configuration exists
     */
    public static function hasConfig(string $browserName): bool
    {
        return isset(self::getAllConfigs()[$browserName]);
    }
